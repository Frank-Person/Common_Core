#include "libft.h"

char	*ft_strdup(const char *str)
{
	int		i;
	char	*dup;

	i = -1;
	dup = malloc((ft_strlen(str) + 1) * sizeof(char));
	if (!dup)
		return (NULL);
	while (str[++i])
		dup[i] = str[i];
	dup[i] = '\0';
	return (dup);
}
