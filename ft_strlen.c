#include <stdio.h>
#include "libft.h"

size_t	ft_strlen(const char *s)
{
	size_t	i;

	i = 0;
	while (s[i])
		i++;
	return (i);
}

int	main(int ac, const char **av)
{
	if (ac == 2)
		printf("%lu\n", ft_strlen(av[1]));
	return (0);
}
