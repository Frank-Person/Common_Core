#include "libft.h"

int	ft_atoi(const char *n)
{
	int	r;
	int	s;

	r = 0;
	s = 1;
	while (*n == ' ' || (*n >= '\t' && *n <= '\r'))
		n++;
	if (*n == '-' || *n == '+')
	{
		if (*n == '-')
			s *= -1;
		n++;
	}
	while (ft_isdigit(*n))
	{
		r = (r * 10) + (*n - '0');
		n++;
	}
	return (r * s);
}
